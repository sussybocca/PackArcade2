use crate::ast::{JovaStatement, JovaExpression};

pub struct Transformer;

impl Transformer {
    pub fn transform(statements: &[JovaStatement]) -> String {
        let mut output = String::new();
        
        output.push_str("// Generated by JOVA Compiler\n");
        output.push_str("// Runtime v1.0\n\n");
        
        // Add runtime helpers
        output.push_str(&Self::runtime_helpers());
        
        for stmt in statements {
            output.push_str(&Self::transform_statement(stmt));
            output.push('\n');
        }
        
        output
    }
    
    fn runtime_helpers() -> String {
        r#"
// JOVA Runtime
const JOVA = {
    _modules: new Map(),
    
    import: function(source) {
        console.log(`[JOVA] Importing ${source}`);
        // In production, this would load the module
    },
    
    exportAll: function() {
        console.log("[JOVA] Exporting all modules");
    },
    
    createModule: function(name, exports) {
        this._modules.set(name, exports);
        return exports;
    },
    
    getModule: function(name) {
        return this._modules.get(name);
    }
};
"#.to_string()
    }
    
    fn transform_statement(stmt: &JovaStatement) -> String {
        match stmt {
            JovaStatement::Import { source } => {
                format!("JOVA.import({});", source)
            }
            JovaStatement::Module { name, body } => {
                let exports = Self::transform_module_body(body);
                format!(
                    "JOVA.createModule('{}', {{\n{}\n}});",
                    name,
                    exports
                )
            }
            JovaStatement::Assignment { name, value } => {
                format!("const {} = {};", name, Self::transform_expression(value))
            }
            JovaStatement::ExportAll => {
                "JOVA.exportAll();".to_string()
            }
            JovaStatement::Command { name, args } => {
                format!("// Command: {} with args {:?}", name, args)
            }
        }
    }
    
    fn transform_module_body(body: &[JovaStatement]) -> String {
        body.iter()
            .map(|stmt| {
                match stmt {
                    JovaStatement::Assignment { name, value } => {
                        format!("  {}: {},", name, Self::transform_expression(value))
                    }
                    _ => format!("  // {}", Self::transform_statement(stmt)),
                }
            })
            .collect::<Vec<_>>()
            .join("\n")
    }
    
    fn transform_expression(expr: &JovaExpression) -> String {
        match expr {
            JovaExpression::StringLiteral(s) => format!("\"{}\"", s),
            JovaExpression::NumberLiteral(n) => n.to_string(),
            JovaExpression::Identifier(id) => id.clone(),
            JovaExpression::BinaryOp { left, op, right } => {
                format!(
                    "{} {} {}",
                    Self::transform_expression(left),
                    op,
                    Self::transform_expression(right)
                )
            }
            JovaExpression::Call { name, args } => {
                let args_str = args.iter()
                    .map(|a| Self::transform_expression(a))
                    .collect::<Vec<_>>()
                    .join(", ");
                format!("{}({})", name, args_str)
            }
        }
    }
}
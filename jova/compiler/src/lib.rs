use wasm_bindgen::prelude::*;
use std::collections::HashMap;
use regex::Regex;

// Initialize console logger when in browser
#[wasm_bindgen]
pub fn init_logger() {
    console_log::init_with_level(log::Level::Info).unwrap();
    log::info!("JOVA Compiler initialized");
}

// Use JsValue for complex types instead of structs
#[wasm_bindgen]
pub struct JovaCompiler {
    modules: HashMap<String, JsValue>,
    exports: Vec<String>,
}

#[wasm_bindgen]
impl JovaCompiler {
    #[wasm_bindgen(constructor)]
    pub fn new() -> JovaCompiler {
        JovaCompiler {
            modules: HashMap::new(),
            exports: Vec::new(),
        }
    }
    
    pub fn compile(&mut self, source: &str) -> String {
        let result = self.compile_internal(source);
        result
    }
    
    fn compile_internal(&mut self, source: &str) -> String {
        let mut output_lines = Vec::new();
        let mut errors = Vec::new();
        
        output_lines.push("// Generated by JOVA Compiler".to_string());
        output_lines.push("// Runtime v1.0\n".to_string());
        
        for line in source.lines() {
            let line = line.trim();
            if line.is_empty() || line.starts_with("//") {
                continue;
            }
            
            match self.process_line(line, &mut output_lines) {
                Ok(_) => {}
                Err(e) => errors.push(e),
            }
        }
        
        if !errors.is_empty() {
            output_lines.push("// ERRORS:".to_string());
            for err in errors {
                output_lines.push(format!("// ❌ {}", err));
            }
        }
        
        // Add runtime code
        output_lines.push("\n// JOVA Runtime".to_string());
        output_lines.push(self.runtime_code());
        
        output_lines.join("\n")
    }
    
    fn process_line(&mut self, line: &str, output: &mut Vec<String>) -> Result<(), String> {
        if line.starts_with("JOVA:") {
            self.handle_jova_command(&line[5..], output)
        } else if line.starts_with("Pop:") {
            self.handle_pop_statement(&line[4..], output)
        } else {
            Err(format!("Unknown syntax: {}", line))
        }
    }
    
    fn handle_jova_command(&mut self, cmd: &str, output: &mut Vec<String>) -> Result<(), String> {
        if cmd.starts_with("Import Pop") {
            let source = cmd.replace("Import Pop", "").replace(".", "").trim().to_string();
            output.push(format!("// Importing: {}", source));
            output.push(format!("JOVA.import('{}');", source));
            Ok(())
        }
        else if cmd == "Export All Pops and Models" || cmd == "Export All Pops and Models." {
            output.push("// Exporting all modules".to_string());
            output.push("JOVA.exportAll();".to_string());
            for name in self.modules.keys() {
                self.exports.push(name.clone());
            }
            Ok(())
        }
        else if cmd.contains("New Language") {
            let parts: Vec<&str> = cmd.split('-').collect();
            if parts.len() >= 2 {
                let name = parts[1].trim().replace(".", "");
                output.push(format!("// Creating new language: {}", name));
                output.push(format!("JOVA.createLanguage('{}');", name));
                self.modules.insert(name.clone(), JsValue::from_str("language"));
            }
            Ok(())
        }
        else {
            Err(format!("Unknown JOVA command: {}", cmd))
        }
    }
    
    fn handle_pop_statement(&mut self, stmt: &str, output: &mut Vec<String>) -> Result<(), String> {
        if stmt.starts_with("New module") {
            let after = stmt.replace("New module", "").replace("-", "").trim().to_string();
            let parts: Vec<&str> = after.split_whitespace().collect();
            if parts.len() >= 2 {
                let name = parts[0];
                let value = parts[1..].join(" ");
                output.push(format!("// Creating module: {} = {}", name, value));
                output.push(format!("JOVA.createModule('{}', '{}');", name, value));
                self.modules.insert(name.to_string(), JsValue::from_str(&value));
                return Ok(());
            }
        }
        Err(format!("Unknown Pop statement: {}", stmt))
    }
    
    fn runtime_code(&self) -> String {
        r#"
const JOVA = {
    _modules: new Map(),
    _languages: new Map(),
    
    import: function(source) {
        console.log(`[JOVA] Importing ${source}`);
    },
    
    exportAll: function() {
        console.log("[JOVA] Exporting all modules");
        for (const [name, module] of this._modules) {
            console.log(`  - Module: ${name}`);
        }
    },
    
    createModule: function(name, value) {
        this._modules.set(name, { type: 'module', value: value });
        console.log(`[JOVA] Module created: ${name} = ${value}`);
    },
    
    createLanguage: function(name) {
        this._languages.set(name, { created: new Date() });
        console.log(`[JOVA] Language created: ${name}`);
    },
    
    getModules: function() {
        return Array.from(this._modules.keys());
    },
    
    getLanguages: function() {
        return Array.from(this._languages.keys());
    }
};

// Auto-execute on load
if (typeof window !== 'undefined') {
    window.JOVA = JOVA;
}
"#.to_string()
    }
    
    pub fn get_modules(&self) -> String {
        let modules: Vec<String> = self.modules.keys().cloned().collect();
        format!("{:?}", modules)
    }
    
    pub fn get_exports(&self) -> String {
        format!("{:?}", self.exports)
    }
}

impl Default for JovaCompiler {
    fn default() -> Self {
        Self::new()
    }
}